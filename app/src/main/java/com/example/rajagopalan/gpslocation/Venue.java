package com.example.rajagopalan.gpslocation;

/**
 * Created by Rajagopalan on 4/21/2017.
 *
 * Class contains properties and behaviours of an event venue
 */
public class Venue {

    private String name;
    private String city;

    public String getCity() {
        return city;
    }

    public String getName() {
        return name;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public void setName(String name) {
        this.name = name;
    }
}
